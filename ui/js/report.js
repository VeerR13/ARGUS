function drawMiniFlow(canvas, timeline) {
  if (!canvas) return;
  const H   = 80;
  const ctx = resizeCanvas(canvas, H);
  const W   = canvas.offsetWidth;
  const PAD = { top: 6, right: 8, bottom: 16, left: 28 };
  const cW  = W - PAD.left - PAD.right;
  const cH  = H - PAD.top  - PAD.bottom;
  ctx.clearRect(0, 0, W, H);

  const n      = timeline.length;
  const maxVal = Math.max(...timeline) * 1.15;
  const xOf    = (i) => PAD.left + (i / (n - 1)) * cW;
  const yOf    = (v) => PAD.top  + cH - (v / maxVal) * cH;

  // Grid
  ctx.strokeStyle = 'rgba(74,158,255,0.07)';
  ctx.lineWidth   = 1;
  [0, 0.5, 1].forEach(f => {
    const y = PAD.top + f * cH;
    ctx.beginPath(); ctx.moveTo(PAD.left, y); ctx.lineTo(PAD.left + cW, y); ctx.stroke();
    const lbl = Math.round(maxVal - f * maxVal);
    ctx.fillStyle = 'rgba(255,255,255,0.2)';
    ctx.font      = '6px "Share Tech Mono"';
    ctx.textAlign = 'right';
    ctx.fillText(lbl, PAD.left - 4, y + 2);
  });

  // Area
  const grad = ctx.createLinearGradient(0, PAD.top, 0, PAD.top + cH);
  grad.addColorStop(0, 'rgba(74,158,255,0.22)');
  grad.addColorStop(1, 'rgba(74,158,255,0)');

  ctx.beginPath();
  ctx.moveTo(xOf(0), yOf(timeline[0]));
  for (let i = 1; i < n; i++) ctx.lineTo(xOf(i), yOf(timeline[i]));
  ctx.lineTo(xOf(n - 1), PAD.top + cH);
  ctx.lineTo(xOf(0),     PAD.top + cH);
  ctx.closePath();
  ctx.fillStyle = grad;
  ctx.fill();

  ctx.beginPath();
  ctx.strokeStyle = '#4a9eff';
  ctx.lineWidth   = 1.2;
  ctx.moveTo(xOf(0), yOf(timeline[0]));
  for (let i = 1; i < n; i++) ctx.lineTo(xOf(i), yOf(timeline[i]));
  ctx.stroke();

  // X labels
  ctx.fillStyle = 'rgba(255,255,255,0.2)';
  ctx.font      = '6px "Share Tech Mono"';
  ctx.textAlign = 'center';
  [0, Math.floor(n / 2), n - 1].forEach(i => {
    const ts = (i / (n - 1)) * (data?.metadata?.duration_seconds || 222);
    ctx.fillText(formatTime(ts), xOf(i), H - 3);
  });
}

function drawMiniDonut(canvas, composition, total) {
  if (!canvas) return;
  const H      = 80;
  const ctx    = resizeCanvas(canvas, H);
  const W      = canvas.offsetWidth;
  ctx.clearRect(0, 0, W, H);

  const COLORS = ['#4a9eff', '#00e5ff', '#ff9f43', '#ff4d6d', '#a78bfa'];
  const vals   = Object.values(composition);
  const labels = Object.keys(composition);
  const sum    = vals.reduce((a, b) => a + b, 0);

  const cx = W * 0.3;
  const cy = H / 2;
  const r  = Math.min(cx, cy) * 0.78;
  const ri = r * 0.55;

  let angle = -Math.PI / 2;
  vals.forEach((v, i) => {
    const slice = (v / sum) * Math.PI * 2;
    ctx.beginPath();
    ctx.moveTo(cx, cy);
    ctx.arc(cx, cy, r, angle, angle + slice);
    ctx.closePath();
    ctx.fillStyle = COLORS[i % COLORS.length];
    ctx.fill();
    angle += slice;
  });

  ctx.beginPath();
  ctx.arc(cx, cy, ri, 0, Math.PI * 2);
  ctx.fillStyle = 'rgba(10,21,32,0.95)';
  ctx.fill();

  ctx.fillStyle = '#fff';
  ctx.font      = '300 18px "Cormorant Garamond"';
  ctx.textAlign = 'center';
  ctx.fillText(total, cx, cy + 4);

  // Legend
  const lx = W * 0.56;
  const lh = H / (labels.length + 0.5);
  labels.forEach((lbl, i) => {
    const y = (i + 0.5) * lh + lh * 0.3;
    ctx.fillStyle = COLORS[i % COLORS.length];
    ctx.fillRect(lx, y - 4, 6, 6);
    ctx.fillStyle = 'rgba(255,255,255,0.35)';
    ctx.font      = '6px "Share Tech Mono"';
    ctx.textAlign = 'left';
    ctx.fillText(lbl.toUpperCase(), lx + 10, y + 2);
  });
}

// ── Risk score computation ─────────────────────────────────────

function computeRiskScore(d) {
  const { summary } = d;
  const accidentWeight  = 2.5;
  const nearMissWeight  = 0.5;
  const congestionWeight = 0.8;
  const accidents  = summary.accidents || 0;
  const nearMisses = (summary.total_incidents || 0) - accidents;
  const raw = accidents * accidentWeight + nearMisses * nearMissWeight
            + (summary.congestion_index || 0) * congestionWeight;
  return Math.min(10, Math.round(raw * 10) / 10);
}

// ── Risk gauge ────────────────────────────────────────────────

function drawRiskGauge(score) {
  const canvas = document.getElementById('risk-gauge');
  if (!canvas) return;

  const dpr = window.devicePixelRatio || 1;
  const W   = 200;
  const H   = 110;
  canvas.width  = W * dpr;
  canvas.height = H * dpr;
  canvas.style.width  = `${W}px`;
  canvas.style.height = `${H}px`;
  const ctx = canvas.getContext('2d');
  ctx.scale(dpr, dpr);
  ctx.clearRect(0, 0, W, H);

  const cx = W / 2;
  const cy = H - 14;
  const r  = 80;

  // Arc background
  const grad = ctx.createLinearGradient(cx - r, cy, cx + r, cy);
  grad.addColorStop(0,    '#7cffcb');
  grad.addColorStop(0.5,  '#ff9f43');
  grad.addColorStop(1,    '#ff4d6d');

  ctx.beginPath();
  ctx.arc(cx, cy, r, Math.PI, 0, false);
  ctx.lineWidth   = 8;
  ctx.strokeStyle = 'rgba(255,255,255,0.05)';
  ctx.stroke();

  ctx.beginPath();
  ctx.arc(cx, cy, r, Math.PI, 0, false);
  ctx.lineWidth   = 4;
  ctx.strokeStyle = grad;
  ctx.stroke();

  // Needle
  const angle    = Math.PI + (score / 10) * Math.PI;
  const nx       = cx + (r - 12) * Math.cos(angle);
  const ny       = cy + (r - 12) * Math.sin(angle);
  ctx.beginPath();
  ctx.moveTo(cx, cy);
  ctx.lineTo(nx, ny);
  ctx.lineWidth   = 1.5;
  ctx.strokeStyle = '#fff';
  ctx.stroke();

  // Centre dot
  ctx.beginPath();
  ctx.arc(cx, cy, 4, 0, Math.PI * 2);
  ctx.fillStyle = '#fff';
  ctx.fill();

  // Score display
  const scoreEl = document.getElementById('risk-score');
  if (scoreEl) scoreEl.textContent = `${score} / 10`;

  // Badge
  const badgeEl = document.getElementById('risk-badge');
  if (badgeEl) {
    const cls  = score >= 7 ? 'badge-critical' : score >= 4 ? 'badge-warning' : 'badge-success';
    const lbl  = score >= 7 ? 'HIGH RISK'       : score >= 4 ? 'MEDIUM RISK'   : 'LOW RISK';
    badgeEl.innerHTML = `<span class="badge ${cls}">${lbl}</span>`;
  }
}

