// ── Cover ─────────────────────────────────────────────────────

function populateCover(d, filename) {
  const { metadata, summary } = d;
  const safeSet = (id, val) => { const el = document.getElementById(id); if (el) el.textContent = val; };

  const now = new Date();
  safeSet('report-date',      now.toLocaleDateString('en-GB', { day:'2-digit', month:'short', year:'numeric' }).toUpperCase());
  safeSet('meta-filename',    filename);
  safeSet('meta-processing',  `${summary.processing_time_seconds}s`);
  safeSet('meta-confidence',  `${summary.detection_confidence.toFixed(1)}%`);
  safeSet('meta-duration',    formatDuration(metadata.duration_seconds));
  safeSet('meta-vehicles',    summary.total_vehicles);
  safeSet('meta-incidents',   summary.total_incidents);

  const badge = document.getElementById('persona-badge');
  if (badge) badge.textContent = `Report Optimised For: ${PERSONA_LABELS[activePersona] || activePersona}`;

  const footer = document.getElementById('report-footer-text');
  if (footer) {
    const ts = now.toLocaleString('en-GB', { hour:'2-digit', minute:'2-digit', day:'2-digit', month:'short' });
    footer.textContent = `Generated by ARGUS AI · ${ts} · Session ${SESSION_ID}`;
  }
}

