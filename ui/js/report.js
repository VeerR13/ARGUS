async function streamAllSections(d) {
  const persona = PERSONA_LABELS[activePersona] || 'insurance investigator';

  // 01 — Executive summary
  await streamToElement('exec-summary',
    buildPrompt('executive_summary', d, null, persona),
    d,
    MOCK_EXEC_SUMMARY[activePersona] || MOCK_EXEC_SUMMARY.insurance
  );

  // 02 — Stats already populated (no AI needed)

  // 03 — Per-incident analyses (sequential)
  for (const inc of d.incidents) {
    await streamToElement(
      `inc-text-${inc.id}`,
      buildPrompt('incident', d, inc, persona),
      d,
      buildMockIncidentText(inc, activePersona)
    );
    // Small pause between incidents to avoid rate-limiting
    await sleep(200);
  }

  // 04 — Risk assessment
  await streamToElement('risk-narrative',
    buildPrompt('risk', d, null, persona),
    d,
    MOCK_RISK[activePersona] || MOCK_RISK.insurance
  );

  // 05 — Recommendations
  await streamRecommendations(d, persona);
}

async function streamToElement(elementId, prompt, d, mockFallback) {
  const el = document.getElementById(elementId);
  if (!el) return;

  el.classList.remove('loading');
  el.textContent = '';

  const chunks    = [];
  let streamDone  = false;

  try {
    await new Promise((resolve) => {
      const payload = { ...d, _report_prompt: prompt };
      streamClaudeAnalysis(payload, activePersona,
        (chunk) => chunks.push(chunk),
        () => { streamDone = true; resolve(); }
      );
      setTimeout(() => { if (!streamDone) resolve(); }, 6000);
    });
  } catch (e) {
    console.warn('[ARGUS] Stream error for', elementId, e);
  }

  const fullText = (chunks.join('').includes('not connected') || chunks.length === 0)
    ? mockFallback
    : chunks.join('');

  await typewriterEffect(el, fullText, 12);
}

async function streamRecommendations(d, persona) {
  const MOCK_RECS = MOCK_RECOMMENDATIONS[activePersona] || MOCK_RECOMMENDATIONS.insurance;

  for (let i = 0; i < 5; i++) {
    const el = document.getElementById(`rec-text-${i + 1}`);
    if (!el) continue;
    el.classList.remove('loading');
    el.textContent = '';
    await typewriterEffect(el, MOCK_RECS[i] || MOCK_RECS[0], 10);
    await sleep(100);
  }
}

// ── Typewriter ────────────────────────────────────────────────

function typewriterEffect(element, text, speed = 12) {
  return new Promise(resolve => {
    element.textContent = '';
    let i = 0;
    const cursor = document.createElement('span');
    cursor.style.cssText = 'display:inline-block;width:1px;height:1em;background:var(--accent);margin-left:2px;vertical-align:middle;animation:blink 1s step-end infinite;';
    element.appendChild(cursor);

    const tick = () => {
      if (i < text.length) {
        cursor.insertAdjacentText('beforebegin', text[i]);
        i++;
        setTimeout(tick, speed);
      } else {
        cursor.remove();
        resolve();
      }
    };
    tick();
  });
}

// ── Prompt builders ───────────────────────────────────────────

function buildPrompt(type, d, incident, persona) {
  const { metadata, summary } = d;
  switch (type) {
    case 'executive_summary':
      return `Write a 3-paragraph executive summary of this traffic analysis for a ${persona}. First paragraph: overview of the footage and key numbers (${summary.total_vehicles} vehicles, ${summary.total_incidents} incidents, ${formatDuration(metadata.duration_seconds)} duration). Second paragraph: most significant incidents, especially the accident-level incidents. Third paragraph: overall risk assessment and urgency. Be concise and precise.`;

    case 'incident':
      return `In 2–3 concise sentences, analyze incident ${incident.id} at ${formatTimestamp(incident.timestamp_start)} for a ${persona}. Incident: ${incident.description}. Causal factors: ${incident.causal_factors.map(c => `${causalLabel(c.type)} (${Math.round(c.confidence * 100)}%)`).join(', ')}. Vehicles involved: ${incident.vehicles_involved.join(', ') || 'unidentified'}. Be specific about vehicle IDs and confidence levels.`;

    case 'risk':
      return `Based on this analysis with ${summary.total_incidents} incidents including ${summary.accidents} accidents and congestion index ${summary.congestion_index}/10, provide an overall risk score justification and a 2-paragraph risk narrative for a ${persona}. The risk score is ${computeRiskScore(d)}/10. Focus on actionable context.`;

    default:
      return '';
  }
}

// ── Print/PDF ─────────────────────────────────────────────────

function initPrint() {
  const btn = document.getElementById('btn-print');
  if (!btn) return;
  btn.addEventListener('click', () => {
    document.body.classList.add('print-mode');
    window.print();
    setTimeout(() => document.body.classList.remove('print-mode'), 1000);
  });
}

